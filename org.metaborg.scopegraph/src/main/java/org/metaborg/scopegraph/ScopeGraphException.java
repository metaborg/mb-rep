package org.metaborg.scopegraph;

public class ScopeGraphException extends Exception {

    private static final long serialVersionUID = 1L;

    public ScopeGraphException() {
        super();
    }

    public ScopeGraphException(String message, Throwable cause) {
        super(message, cause);
    }

    public ScopeGraphException(String message) {
        super(message);
    }

    public ScopeGraphException(Throwable cause) {
        super(cause);
    }

}
